package com.best.role;

public class RoleDTO {
    private int role_idx;
    private int page_idx;
    private String idx_name;
    private int idx_num;

    public int getRole_idx() {
        return role_idx;
    }

    public void setRole_idx(int role_idx) {
        this.role_idx = role_idx;
    }

    public int getPage_idx() {
        return page_idx;
    }

    public void setPage_idx(int page_idx) {
        this.page_idx = page_idx;
    }

    public String getIdx_name() {
        return idx_name;
    }

    public void setIdx_name(String idx_name) {
        this.idx_name = idx_name;
    }

    public int getIdx_num() {
        return idx_num;
    }

    public void setIdx_num(int idx_num) {
        this.idx_num = idx_num;
    }
}
