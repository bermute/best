package com.best.emp;

public class DepartmentDTO {
	private int depart_idx;
	private String depart_name;
	private int role_idx;
	public int getDepart_idx() {
		return depart_idx;
	}
	public void setDepart_idx(int depart_idx) {
		this.depart_idx = depart_idx;
	}
	public String getDepart_name() {
		return depart_name;
	}
	public void setDepart_name(String depart_name) {
		this.depart_name = depart_name;
	}
	public int getRoleIdx() {
		return role_idx;
	}
	public void setRoleIdx(int roleIdx) {
		this.role_idx = roleIdx;
	}
	
	
}
